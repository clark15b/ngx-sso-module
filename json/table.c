json_table_t* json_table_init(json_pool_t* pool,json_table_t* t,int basis)
{
    size_t i;

    t->pool=pool;

    switch(basis)
    {
    case JSON_HASH_256_95:
        t->symbols=json_hash_symbols_95;
        t->table=json_hash_table_256;
        t->nelts=256;
        break;

    case JSON_HASH_256_64:
        t->symbols=json_hash_symbols_64;
        t->table=json_hash_table_256;
        t->nelts=256;
        break;

    case JSON_HASH_256_38:
        t->symbols=json_hash_symbols_38;
        t->table=json_hash_table_256;
        t->nelts=256;
        break;

    case JSON_HASH_95:
        t->symbols=json_hash_symbols_95;
        t->table=json_hash_table_95;
        t->nelts=95;
        break;

    case JSON_HASH_64:
        t->symbols=json_hash_symbols_64;
        t->table=json_hash_table_64;
        t->nelts=64;
        break;

    case JSON_HASH_38:
        t->symbols=json_hash_symbols_38;
        t->table=json_hash_table_38;
        t->nelts=38;
        break;

    default:
        t->symbols=NULL;
        t->table=json_hash_table_256;
        t->nelts=256;
        break;
    }

    t->elts=json_palloc(t->pool,t->nelts*sizeof(json_table_list_t));

    if(!t->elts)
        return NULL;

    for(i=0;i<t->nelts;++i)
        { t->elts[i].first=t->elts[i].last=NULL; }

    t->size=0;
    t->user_data=NULL;
    t->zero_copy=0;

    return t;
}

json_table_t* json_table_new(json_pool_t* pool,int basis)
{
    json_table_t* t=json_palloc(pool,sizeof(json_table_t));

    if(!t)
        return NULL;

    return json_table_init(pool,t,basis);
}

// вычисление ключа с использованием алгоритма хеширования Пирсона
static u_int32_t json_table_hash(json_table_t* t,const u_int8_t* key,size_t klen)
{
    if(!t->table || klen<1)
        return 0xffffffff;

    u_int32_t hash=klen % t->nelts; size_t i;

    if(t->symbols)
    {
        for(i=0;i<klen;++i)
        {
            int c=t->symbols[key[i]];

            if(c==0xff)
                return 0xffffffff;

            hash=t->table[ (hash + c) % t->nelts ];
        }
    }else
    {
        for(i=0;i<klen;++i)
            hash=t->table[ (hash ^ key[i]) % t->nelts ];
    }

    return hash;
}

static int json_memcmp_lc(const void* s1,const void* s2,size_t n)
{
    size_t i;

    for(i=0;i<n;++i)
    {
        if(tolower(((unsigned char*)s1)[i])!=tolower(((unsigned char*)s2)[i]))
            return -1;
    }

    return 0;
}

json_table_elt_t* json_table_find(json_table_t* t,const u_int8_t* key,size_t klen,int no_create)
{
    u_int32_t hash=json_table_hash(t,key,klen);

    if(hash>=t->nelts)
        return NULL;

    json_table_list_t* elts=&t->elts[hash];

    json_table_elt_t* elt=elts->first;

    while(elt)
    {
        if(elt->key.data && elt->key.len==klen &&
            !(t->symbols==json_hash_symbols_38 ? json_memcmp_lc(elt->key.data,key,klen) : memcmp(elt->key.data,key,klen)))
                return elt;

        elt=elt->next;
    }

    if(no_create)
        return NULL;

    if(t->zero_copy)
    {
        elt=json_palloc(t->pool,sizeof(json_table_elt_t));

        if(!elt)
            return NULL;

        elt->key.data=(u_int8_t*)key;
        elt->key.len=klen;

    }else
    {
        elt=json_palloc(t->pool,sizeof(json_table_elt_t)+klen+1);

        if(!elt)
            return NULL;

        elt->key.data=((u_int8_t*)elt)+sizeof(json_table_elt_t);
        elt->key.len=klen;
        json_memcpy(elt->key.data,key,klen);
        elt->key.data[klen]=0;
    }

    elt->next=NULL;
    elt->user_data=NULL;
    elt->idx=0xffffffff;

    if(!elts->first)
        elts->first=elts->last=elt;
    else
        { elts->last->next=elt; elts->last=elt; }

    return elt;
}

json_table_elt_t* json_table_set(json_table_t* t,const u_int8_t* key,size_t klen,const u_int8_t* data,size_t len)
{
    json_table_elt_t* elt=json_table_find(t,key,klen,0);

    if(!elt)
        return NULL;

    return json_elt_set(t,elt,data,len);
}

json_table_t* json_array_init(json_pool_t* pool,json_table_t* t,u_int32_t table_size)
{
    u_int32_t i;

    t->pool=pool;
    t->symbols=NULL;
    t->table=NULL;
    t->nelts=table_size;

    t->elts=json_palloc(t->pool,t->nelts*sizeof(json_table_list_t));

    if(!t->elts)
        return NULL;

    for(i=0;i<t->nelts;++i)
        { t->elts[i].first=t->elts[i].last=NULL; }

    t->size=0;
    t->user_data=NULL;
    t->zero_copy=0;

    return t;
}

json_table_t* json_array_new(json_pool_t* pool,u_int32_t table_size)
{
    json_table_t* t=json_palloc(pool,sizeof(json_table_t));

    if(!t)
        return NULL;

    return json_array_init(pool,t,table_size);
}

json_table_elt_t* json_array_find(json_table_t* t,u_int32_t idx,int no_create)
{
    u_int32_t hash=idx % t->nelts;

    json_table_list_t* elts=&t->elts[hash];

    json_table_elt_t* elt=elts->first;

    while(elt)
    {
        if(elt->idx==idx)
            return elt;

        elt=elt->next;
    }

    if(no_create)
        return NULL;

    elt=json_palloc(t->pool,sizeof(json_table_elt_t));

    if(!elt)
        return NULL;

    elt->idx=idx;
    elt->key.data=NULL;
    elt->key.len=0;
    elt->next=NULL;
    elt->user_data=NULL;

    if(!elts->first)
        elts->first=elts->last=elt;
    else
        { elts->last->next=elt; elts->last=elt; }

    if(idx>=t->size)
        t->size=idx+1;

    return elt;
}

json_table_elt_t* json_array_set(json_table_t* t,u_int32_t idx,const u_int8_t* data,size_t len)
{
    json_table_elt_t* elt=json_array_find(t,idx,0);

    if(!elt)
        return NULL;

    return json_elt_set(t,elt,data,len);
}

json_table_elt_t* json_array_push(json_table_t* t)
{
    return json_array_find(t,t->size,0);
}

json_table_elt_t* json_elt_set(json_table_t* t,json_table_elt_t* elt,const u_int8_t* data,size_t len)
{
    if(len>0)
    {
        if(t->zero_copy)
        {
            elt->value.data=(u_int8_t*)data;
            elt->value.len=len;

            return elt;
        }else
        {
            elt->value.data=json_palloc(t->pool,len+1);

            if(elt->value.data)
            {
                json_memcpy(elt->value.data,data,len);

                elt->value.data[len]=0;
                elt->value.len=len;

                return elt;
            }
        }
    }

    elt->value.data=NULL;
    elt->value.len=0;

    return NULL;
}

int json_is_table(json_table_t* t)
{
    return t->table?1:0;
}

int json_is_array(json_table_t* t)
{
    return t->table?0:1;
}
